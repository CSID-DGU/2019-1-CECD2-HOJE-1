package Com.Fasoo.Utilization;

public class Version {
    int Major_version;
    int Minor_version;
    int Minor_version2;

    public int getMajor_version() {
        return Major_version;
    }

    public int getMinor_version() {
        return Minor_version;
    }

    public int getMinor_version2() {
        return Minor_version2;
    }

    public void setMajor_version(int major_version) {
        Major_version = major_version;
    }

    public void setMinor_version(int minor_version) {
        Minor_version = minor_version;
    }

    public void setMinor_version2(int minor_version2) {
        Minor_version2 = minor_version2;
    }
}
